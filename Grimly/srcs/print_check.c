#include "grimly.h"

void	print_check(t_key *key)
{
	int n;
	int i;

	n = 0;
	i = 0;
	while (n < key->row)
	{
		while (i < key->column)
			ft_putchar(key->arr[n][i++]);
		write(1, "\n", 1);
		i = 0;
		n++;
	}
	write(1, "\n", 1);
}
